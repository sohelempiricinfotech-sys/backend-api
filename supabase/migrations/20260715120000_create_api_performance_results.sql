    ('POST', '/api/jobs/generate-jd',    160::numeric,22::numeric,1250::numeric,360::numeric,1900::numeric,510::numeric, 18000,2500, 0.070::numeric, 4000, 0.0040::numeric, 4),
    ('GET',  '/api/candidates',           92::numeric, 9::numeric, 188::numeric, 20::numeric, 340::numeric, 35::numeric, 450000, 300, 0.025::numeric, 5000, 0.0002::numeric, 5),
    ('GET',  '/api/candidates/export',   250::numeric,40::numeric,1200::numeric,250::numeric,2500::numeric,500::numeric,   7500,2000, 0.060::numeric,15000, 0.0002::numeric, 6),
    ('GET',  '/health',                    10::numeric, 1::numeric,  23::numeric,  3::numeric,  40::numeric,  5::numeric,1200000, 100, 0.0005::numeric, 1000, 0.0000::numeric, 7),
    ('POST', '/api/candidates',          190::numeric,15::numeric, 400::numeric, 50::numeric, 600::numeric, 80::numeric,  40000,1000, 0.010::numeric, 5000, 0.0001::numeric, 8),
    ('POST', '/api/auth/login',           55::numeric, 5::numeric, 140::numeric, 16::numeric, 240::numeric, 30::numeric, 900000, 150, 0.008::numeric, 2000, 0.0001::numeric, 9),
    ('POST', '/api/auth/refresh-token',   45::numeric, 5::numeric, 120::numeric, 15::numeric, 200::numeric, 25::numeric,1200000, 150, 0.007::numeric, 2000, 0.0001::numeric,10)
  ) as e(method, route_template, p50_center, p50_sd, p95_center, p95_sd, p99_center, p99_sd, requests_24h, slow_threshold_ms, slow_rate, timeout_threshold_ms, timeout_rate, phase)
), dates as (
  select generate_series(date '2026-04-01', date '2026-06-30', interval '1 day')::date as d
), monitor_rows as (
  select
    e.*, d.d,
    sqrt(29::numeric / 30::numeric) as sample_adjustment,
    ((extract(doy from d.d)::integer + e.phase) % 30) as p50_phase,
    ((extract(doy from d.d)::integer + e.phase) % 30) as p95_phase,
    ((extract(doy from d.d)::integer + e.phase) % 30) as p99_phase
  from endpoint_seed e cross join dates d
  where e.route_template <> '/api/auth/refresh-token'
     or d.d between date '2026-06-01' and date '2026-06-28'
)
insert into public.api_performance_results (
  measurement_at, environment, source, status, method, route_template,
  p50_ms, p95_ms, p99_ms, sample_count, requests_24h, slow_threshold_ms,
  slow_rate, timeout_threshold_ms, timeout_rate, fixture_key, metadata
)
select
  (d + time '12:00:00') at time zone 'Asia/Kolkata',
  'production', 'synthetic_monitor', 'complete', method, route_template,
  case when p50_phase < 15 then p50_center - p50_sd * sample_adjustment else p50_center + p50_sd * sample_adjustment end,
  case when p95_phase < 15 then p95_center - p95_sd * sample_adjustment else p95_center + p95_sd * sample_adjustment end,
  case when p99_phase < 15 then p99_center - p99_sd * sample_adjustment else p99_center + p99_sd * sample_adjustment end,
  10000, requests_24h, slow_threshold_ms, slow_rate, timeout_threshold_ms, timeout_rate,
  format('baseline:%s:%s:%s', method, route_template, d),
  jsonb_build_object('fixture_mode', true, 'kind', 'rolling-baseline-monitor')
from monitor_rows
on conflict (fixture_key) do nothing;

