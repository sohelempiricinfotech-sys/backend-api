-- Fixture schema and deterministic production history for API performance-regression analysis.
-- Each row is a daily aggregated benchmark: p50_ms, p95_ms, and p99_ms are
-- already response-time percentiles. See PERFORMANCE_REGRESSION_FIXTURES.md
-- for the required rolling-baseline interpretation.

create table if not exists public.api_performance_results (
  id bigint generated by default as identity primary key,
  measurement_at timestamptz not null,
  environment text not null check (environment in ('production', 'staging', 'development')),
  source text not null check (source in ('synthetic_monitor', 'github_actions', 'supertest', 'load_test_staging')),
  status text not null default 'complete' check (status in ('complete', 'failed', 'invalid')),
  method text not null,
  route_template text not null,
  p50_ms numeric not null check (p50_ms >= 0),
  p95_ms numeric not null check (p95_ms >= p50_ms),
  p99_ms numeric not null check (p99_ms >= p95_ms),
  sample_count integer not null check (sample_count > 0),
  requests_24h integer not null check (requests_24h >= 0),
  slow_threshold_ms integer not null check (slow_threshold_ms > 0),
  slow_rate numeric not null check (slow_rate >= 0 and slow_rate <= 1),
  timeout_threshold_ms integer not null check (timeout_threshold_ms > slow_threshold_ms),
  timeout_rate numeric not null check (timeout_rate >= 0 and timeout_rate <= 1),
  git_sha text,
  pr_number integer,
  workflow_run_id text,
  artifact_name text,
  fixture_key text not null unique,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists api_performance_results_baseline_idx
  on public.api_performance_results (method, route_template, measurement_at desc)
  where environment = 'production' and source = 'synthetic_monitor' and status = 'complete';

create index if not exists api_performance_results_pr_idx
  on public.api_performance_results (pr_number, measurement_at desc)
  where pr_number is not null;

alter table public.api_performance_results enable row level security;
revoke all on table public.api_performance_results from anon, authenticated;
grant select, insert, update, delete on table public.api_performance_results to service_role;
grant usage, select on sequence public.api_performance_results_id_seq to service_role;
drop policy if exists "service role manages API performance fixtures" on public.api_performance_results;
