-- 30-day alternating distributions give every full rolling 30-day window a
-- deterministic arithmetic mean and sample standard deviation. The small
-- phase offsets keep percentile columns from moving in lockstep.
with endpoint_seed as (
  select * from (values
    ('POST', '/api/dashboard/stats',      70::numeric, 4::numeric, 150::numeric, 10::numeric, 235::numeric, 16::numeric, 180000, 250, 0.010::numeric, 4000, 0.0001::numeric, 1),
